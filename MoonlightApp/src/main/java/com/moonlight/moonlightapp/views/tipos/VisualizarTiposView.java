    private void formWindowOpened(java.awt.event.WindowEvent evt) {// GEN-FIRST:event_formWindowOpened
        DefaultTableModel tabelaTiposProdutos = (DefaultTableModel) tbTiposProdutos.getModel();
        DefaultTableModel tabelaTiposMateriasPrimas = (DefaultTableModel) tbTiposMateriasPrimas.getModel();

        tipoProdutoDAO.buscarTodos().forEach(tp -> {
            var tipoProdutoObj = new Object[] {
                    tp.getNome()
            };

            tabelaTiposProdutos.addRow(tipoProdutoObj);
        });

        tipoMateriaPrimaDAO.buscarTodos().forEach(tm -> {
            var tipoMateriaPrimaObj = new Object[] {
                    tm.getNome()
            };

            tabelaTiposMateriasPrimas.addRow(tipoMateriaPrimaObj);
        });
    }// GEN-LAST:event_formWindowOpened

