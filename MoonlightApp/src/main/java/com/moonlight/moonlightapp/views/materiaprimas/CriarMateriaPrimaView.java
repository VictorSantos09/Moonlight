    private void formWindowOpened(java.awt.event.WindowEvent evt) {// GEN-FIRST:event_formWindowOpened
        cbUnidadeMedida.removeAll();

        unidadeMedidaDAO.buscarTodos().forEach(um -> cbUnidadeMedida.addItem(um));

        cbTipo.removeAll();

        tipoMateriaPrimaDAO.buscarTodos().forEach(tp -> cbTipo.addItem(tp));
    }// GEN-LAST:event_formWindowOpened

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnConfirmarActionPerformed
        var nome = txtNomeMateriaPrima.getText();
        var descricao = txtDescricaoMateriaPrima.getText();
        var valor = Double.parseDouble(txtValor.getText());
        var quantidade = Integer.parseInt(txtQuantidade.getText());

        var tipo = (TipoMateriaPrimaModel) cbTipo.getSelectedItem();
        var unidade = (UnidadeMedidaModel) cbUnidadeMedida.getSelectedItem();
        MateriaPrimaDTO materiaPrimaDTO = new MateriaPrimaDTO(nome, descricao, valor, quantidade, unidade.getSigla(),
                tipo.getNome());
        
        var resultado = criarMateriaPrimaService.CriarMateriaPrima(materiaPrimaDTO);
        JOptionPane.showMessageDialog(null, resultado.getMensagem());

    }// GEN-LAST:event_btnConfirmarActionPerformed

