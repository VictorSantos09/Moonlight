    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnBuscarActionPerformed
        var produtoSelecionado = (ProdutoModel) cbProdutos.getSelectedItem();

        if (produtoSelecionado == null) {
            JOptionPane.showMessageDialog(null, "selecione um produto");
        }
        var itensProdutosNecessarios = itensProdutoDAO.buscarPorProdutoId(produtoSelecionado.getId());

        if (itensProdutosNecessarios == null || itensProdutosNecessarios.isEmpty()) {
            JOptionPane.showMessageDialog(null, "nenhum item produto encontrado para esse projeto");
        }

        DefaultTableModel tabela = (DefaultTableModel) tbProdutoItens.getModel();
        tabela.setRowCount(0);

        for (var ip : itensProdutosNecessarios) {
            Object[] itemProdutoObj = new Object[] {
                    ip.getMateriaPrima().getNome(),
                    ip.getMateriaPrima().getTipoMateriaPrima().getNome(),
                    ip.getMateriaPrima().getUnidadeMedida().getNome(),
                    ip.getQuantidade(),
                    ip.getMateriaPrima().getValor(),
                    ip.getSubTotal()
            };

            tabela.addRow(itemProdutoObj);
        }
    }// GEN-LAST:event_btnBuscarActionPerformed

