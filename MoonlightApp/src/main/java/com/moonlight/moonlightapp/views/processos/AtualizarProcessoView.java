    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnSalvarActionPerformed
        var processoSelecionado = (ProcessoModel) cbProcessos.getSelectedItem();
        var etapa = txtEtapa.getText();
        var custo = 0d;
        
        try {
            custo = Double.parseDouble(txtCusto.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "custo invÃ¡lido");
        }

        var dto = new AtualizarProcessoDTO(processoSelecionado.getEtapa(), etapa, custo);
        var resultado = atualizarProcessoService.atualizar(dto);

        JOptionPane.showMessageDialog(null, resultado.getMensagem());
    }// GEN-LAST:event_btnSalvarActionPerformed

    private void cbProcessosItemStateChanged(java.awt.event.ItemEvent evt) {// GEN-FIRST:event_cbProcessosItemStateChanged
        var processoSelecionado = (ProcessoModel) cbProcessos.getSelectedItem();

        txtEtapa.setText(processoSelecionado.getEtapa());
        txtCusto.setText(String.valueOf(processoSelecionado.getCusto()));
    }// GEN-LAST:event_cbProcessosItemStateChanged

    private void formWindowOpened(java.awt.event.WindowEvent evt) {// GEN-FIRST:event_formWindowOpened
        buscarProcessoService.buscarTodos().forEach(p -> {
            cbProcessos.addItem(p);
        });
    }// GEN-LAST:event_formWindowOpened

